package ch.bemar.dhcp.exception;

public class InterfaceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3009963920064719254L;

	public InterfaceException() {
		super();

	}

	public InterfaceException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public InterfaceException(String message, Throwable cause) {
		super(message, cause);

	}

	public InterfaceException(String message) {
		super(message);

	}

	public InterfaceException(Throwable cause) {
		super(cause);

	}

}
